#include "main.h"
#include <string.h>
#include <stdio.h>
/**
 * _strspn - prefix substring
 * @s: string to check
 * @accept: number of bytes
 * Return: returns values
 */
unsigned int _strspn(char *s, char *accept)
{
	int i;
	int j;
	unsigned int result = 0;

	for (i = 0; s[i] != '\0'; i++)
	{
		for (j = 0; s[j] != '\0'; j++)
		{
			if (s[i] == accept[j])
			{
				result++;
				break;
			}
		}
		if (s[j] == '\0')
			return (result);
	}
	return (result);
}
